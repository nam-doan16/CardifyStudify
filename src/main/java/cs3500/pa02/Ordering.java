package cs3500.pa02;

/**
 * Represented enumerated type for file sort ordering:
 * FILENAME - Represents files to be sorted by filename (ascending)
 * CREATED - Represents files to be sorted by file creation time (ascending)
 * MODIFIED - Represents files to be sorted by file modification time (ascending)
 */
public enum Ordering {
  FILENAME, CREATED, MODIFIED
}
